// 使用SPI发送一个字节的数据
u8 SPI_FLASH_SendByte(u8 byte)
{
	 SPITimeout = SPIT_FLAG_TIMEOUT; // 等待超时时间初始化

  // 等待发送缓冲区为空，TXE事件
  while(SPI_I2S_GetFlagStatus(FLASH_SPIx, SPI_I2S_FLAG_TXE) == RESET)
	{
    if((SPITimeout--) == 0) 
      return SPI_TIMEOUT_UserCallback(0);
   }
  // 写入数据寄存器，把要写入的数据写入发送缓冲区
  SPI_I2S_SendData(FLASH_SPIx, byte);
	SPITimeout = SPIT_FLAG_TIMEOUT;
  // 等待接收缓冲区非空，RXNE事件
  while(SPI_I2S_GetFlagStatus(FLASH_SPIx, SPI_I2S_FLAG_RXNE) == RESET)
  {
    if((SPITimeout--) == 0) 
      return SPI_TIMEOUT_UserCallback(1);
  }
  // 读取数据寄存器，获取接收缓冲区数据
  return SPI_I2S_ReceiveData(FLASH_SPIx);
}

// 使用SPI发送两个字节的数据
u16 SPI_FLASH_SendHalfWord(u16 HalfWord)
{
	SPITimeout = SPIT_FLAG_TIMEOUT;
  // 等待发送缓冲区为空，TXE事件
  while(SPI_I2S_GetFlagStatus(FLASH_SPIx , SPI_I2S_FLAG_TXE) == RESET)
	{
    if((SPITimeout--) == 0) 
      return SPI_TIMEOUT_UserCallback(2);
  }
  // 写入数据寄存器，把要写入的数据写入发送缓冲区
  SPI_I2S_SendData(FLASH_SPIx , HalfWord);
	SPITimeout = SPIT_FLAG_TIMEOUT;
  // 等待接收缓冲区非空，RXNE事件
  while(SPI_I2S_GetFlagStatus(FLASH_SPIx , SPI_I2S_FLAG_RXNE) == RESET)
	 {
    if((SPITimeout--) == 0) 
      return SPI_TIMEOUT_UserCallback(3);
   }
  // 读取数据寄存器，获取接收缓冲区数据 
  return SPI_I2S_ReceiveData(FLASH_SPIx);
}

// 向FLASH写入数据前，先发送写使能命令
void SPI_FLASH_WriteEnable(void)
{
  // 1.通讯开始：CS低
  SPI_FLASH_CS_LOW();
  // 2.发送写使能命令
  SPI_FLASH_SendByte(W25X_WriteEnable);

  // 3.通讯结束：CS高
  SPI_FLASH_CS_HIGH();
}

/* WIP(busy)标志，FLASH内部正在写入 */
#define WIP_Flag                  0x01

// 等待WIP(BUSY)标志被置0，即等待到FLASH内部数据写入完毕
void SPI_FLASH_WaitForWriteEnd(void)
{
  u8 FLASH_Status = 0;

  // 1.选择 FLASH: CS 低
  SPI_FLASH_CS_LOW();

  // 2.发送 读状态寄存器 命令
  SPI_FLASH_SendByte(W25X_ReadStatusReg);

  // 3.若FLASH忙碌，则等待
  do
  {
		/* 读取FLASH芯片的状态寄存器 */
    FLASH_Status = SPI_FLASH_SendByte(Dummy_Byte);	 
  }
  while((FLASH_Status & WIP_Flag) == SET);  /* 正在写入标志 */
  // 4.停止信号  FLASH: CS 高
  SPI_FLASH_CS_HIGH();
}

// 进入掉电模式
void SPI_Flash_PowerDown(void)   
{ 
  /* 通讯开始：CS低 */
  SPI_FLASH_CS_LOW();
  /* 发送 掉电 命令 */
  SPI_FLASH_SendByte(W25X_PowerDown);
  /*通讯结束：CS高 */
  SPI_FLASH_CS_HIGH();
}   

// 唤醒
void SPI_Flash_WAKEUP(void)   
{
  /*选择 FLASH: CS 低 */
  SPI_FLASH_CS_LOW();
  /* 发送 上电 命令 */
  SPI_FLASH_SendByte(W25X_ReleasePowerDown);
  /* 停止信号 FLASH: CS 高 */
  SPI_FLASH_CS_HIGH();
}   
   
// 超时回调函数
static  uint16_t SPI_TIMEOUT_UserCallback(uint8_t errorCode) // 函数参数：错误代码
{
  /* 等待超时后的处理,输出错误信息 */
  FLASH_ERROR("SPI 等待超时!errorCode = %d", errorCode);
  return 0;
}